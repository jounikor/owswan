include mdef.inc

        name    dgroup

ifdef   __OS2__
        extrn   ___nearheap : word
endif

DGROUP  group   _DATA
_DATA   segment word public 'DATA'
_DATA   ends

_TEXT   segment word public 'CODE'
        assume  cs:_TEXT

        public  _DGroup_


        defp    _DGroup_
if _MODEL and _DS_PEGGED
        mov     ax,ds                   ; DS points to DGROUP
else
        mov     ax,DGROUP               ; get segment address of DGROUP
endif
        ret                             ; return
_DGroup_ endp


_TEXT   ends

        end
