#include "variety.h"
#include <dos.h>
#include <windows.h>

_WCRTLINK unsigned sleep( unsigned seconds )
{
    /*
     * Don't use Sleep because on AXPs wlink will get confused trying to
     * export 'sleep' and import 'Sleep', which differ only in case.
     */
    SleepEx( seconds * 1000L, FALSE );
    return( 0 );
}
