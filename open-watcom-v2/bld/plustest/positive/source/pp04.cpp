unsigned calc_hash( char *id, size_t *plen )
{
    unsigned c;
    unsigned g;
    unsigned h;
    size_t len;

    len = 0;
    h = *id;
    h += h + h;
    for(;;) {
        /* ( h & ~0x0fff ) == 0 is always true here */
	c = *(id++);
        if( c == '\0' ) break;
	++len;
	h = ( h << 4 ) + c;
        g = h & ~0x0fff;
	h ^= g;
	h ^= (g >> 12)&0x0f;
    }
    *plen = len;
    return( h );
}

